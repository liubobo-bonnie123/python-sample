# Define our attributes for the HTML E-factory.

def CLASS(*args):
  return {"class":' '.join(args)}

def SRC(*args):
  return {"src":' '.join(args)}

def HREF(*args):
  return {"href":' '.join(args)}
  
def ID(*args):
  return {"id":' '.join(args)}

def STYLE(*args):
  return {"style":' '.join(args)}
